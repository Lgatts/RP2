    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        switch (listaSubmissao.getTipoSubmissao()) {
            case Monografia:
                submissaoSelecionada = new Monografia();
                salvarMonografia();
                break;
            case Resumo:
                submissaoSelecionada = new Resumo();
                salvarResumo();
                break;
            case Palestra:
                submissaoSelecionada = new Palestra();
                salvarPalestra();
                break;
            case Minicurso:
                submissaoSelecionada = new Minicurso();
                salvarMinicurso();
                break;
            case Artigo:
                submissaoSelecionada = new Artigo();
                salvarArtigo();
                break;
        }

        if (isEdit) {
            listaSubmissao.editar(tituloAnterior, submissaoSelecionada);
        } else {
            listaSubmissao.incluir(submissaoSelecionada);
        }

        this.dispose();
        new ListarFrame(listaSubmissao).setVisible(true);

    }//GEN-LAST:event_jButtonSalvarActionPerformed

