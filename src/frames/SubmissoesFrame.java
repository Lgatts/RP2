    private void layoutMonografia() {
        jPanelSubmissaoApresentacao.removeAll();
        jPanelBotoesAutores.removeAll();
        jPanelBotoesInstituicoes.removeAll();
        jPanelMinicurso.removeAll();
        jPanelPalestra.removeAll();

        for (Tipo e : Tipo.values()) {
            jComboBoxTipo.addItem(e.getTipo());
        }

        if (isEdit) {
            setEditarSubmissaoCientifica();

            Monografia submissao = (Monografia) submissaoSelecionada;
            jTextFieldAno.setText(String.valueOf(submissao.getAno()));
            jTextFieldCurso.setText(submissao.getCurso());
            jTextFieldOrientador.setText(submissao.getOrientador());
            jTextFieldNPaginas.setText(String.valueOf(submissao.getnPaginas()));

            for (int i = 0; i < 4; i++) {
                if (jComboBoxTipo.getItemAt(i).equalsIgnoreCase(submissao.getTipo().toString())) {
                    jComboBoxTipo.setSelectedIndex(i);
                }
            }

            jTextAreaAbstract.setText(submissao.getAbstractText());
            jTextAreaResumo.setText(submissao.getResumo());
        }

    }

